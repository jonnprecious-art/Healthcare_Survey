def index():
    return render_template('index.html')

# Route for form submission
@app.route('/submit', methods=['POST'])
def submit():
    # Get form data
    age = request.form.get('age')
    gender = request.form.get('gender')
    income = request.form.get('income')
    utilities = request.form.get('utilities')
    entertainment = request.form.get('entertainment')
    school_fees = request.form.get('school_fees')
    shopping = request.form.get('shopping')
    healthcare = request.form.get('healthcare')

    # Save to CSV
    file_exists = os.path.isfile('users.csv')
    with open('users.csv', 'a', newline='') as csvfile:
        writer = csv.writer(csvfile)
        # write header only if file is new
        if not file_exists:
            writer.writerow(['Age','Gender','Income','Utilities','Entertainment','School Fees','Shopping','Healthcare'])
        writer.writerow([age,gender,income,utilities,entertainment,school_fees,shopping,healthcare])

    return "Data submitted successfully!"

