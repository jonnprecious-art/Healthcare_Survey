from flask import Flask, render_template, request, redirect
from pymongo import MongoClient
import csv
from pyngrok import ngrok, conf

app = Flask(__name__)

# MongoDB connection
client = MongoClient('mongodb://localhost:27017/')
db = client["survey_db"]
collection = db["users"]

class User:
  def __init__(self, age, gender, income, expenses):
    self.age = age
    self.gender = gender
    self.income = income
    self.expenses = expenses

@app.route('/')
def index():
  return render_template('index.html')

@app.route('/submit', methods=['POST'])
def submit():
  age = request.form['age']
  gender = request.form['gender']
  income = request.form['income']

  expenses = {
      "utilities": request.form.get('utilities', 0),
      "entertainment": request.form.get('entertainment'),
      "school_fees": request.form.get('school_fees', 0),
      "shopping": request.form.get('shopping', 0),
      "healthcare": request.form.get('healthcare', 0),
      "transportation": request.form.get('transportation', 0),
  }

  user = User(age, gender, income, expenses)

  # Save to MongoDB
  collection.insert_one({
      "age": user.age,
      "gender": user.gender,
      "income": user.income,
      "expenses": user.expenses

  })

  return redirect('/')

@app.route('/export')
def export():
  users = list(collection.find())

  with open('data/users.csv', 'w', newline='') as file:
    writer = csv.writer(file)
    writer.writerow([
        "Age", "Gender", "Income",
        "Utilities", "Entertainment",
        "School Fees", "Shopping", "Healthcare"
    ])

    for user in users:
      writer.writerow([
          user["age"],
          user["gender"],
          user["income"],
          user["expenses"].get("utilities", 0),
          user["expenses"].get("entertainment", 0),
          user["expenses"].get("school_fees", 0),
          user["expenses"].get("shopping", 0),
          user["expenses"].get("healthcare", 0)
        ])

  return "CSV Exported Successfully!"

